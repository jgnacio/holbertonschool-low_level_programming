#include "3-calc.h"
/**
 * op_add - Function that calculate the sum of a and b.
 * @a: Firts int.
 * @b: Second int.
 *
 * Return: The sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Function that calculate the difference of a and b.
 * @a: Firts int.
 * @b: Second int.
 *
 * Return: The difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Function that calculate the product of a and b.
 * @a: Firts int.
 * @b: Second int.
 *
 * Return: The product of a by b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Function that calculate the division of a by b.
 * @a: Firts int.
 * @b: Second int.
 *
 * Return: The result of the division of a by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function that calculate the module of a by b.
 * @a: Firts int.
 * @b: Second int.
 *
 * Return: The remainder of the division of a by b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
